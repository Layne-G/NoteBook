# 基本数据类型

#示例1  路程 = 速度 * 时间
speed = 12.8  #以m/s为单位，浮点数
time = 30  #以秒为单位，整数
#路程s
s = speed*time
print(s)

#根据Python中一切皆是对象
intA = int(10)  #整数
floatB = float(3.14)  #浮点数
complexC = complex(3,5)  #复数
print(intA)
print(floatB)
print(complexC)

boolA= bool(True)  #布尔类型，值为True
boolB = bool(False) #布尔类型，值为False
print(boolA)
print(boolB)
